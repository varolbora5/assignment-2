WITH
	TICKET_COUNT AS (
		SELECT
			COUNT(EVENT) AS TICKET_COUNT,
			EVENT
		FROM
			TICKETS
		GROUP BY
			EVENT
		ORDER BY
			TICKET_COUNT DESC
	)
SELECT
	NAME
FROM
	EVENTS
	JOIN TICKET_COUNT ON EVENTS.NAME = TICKET_COUNT.EVENT
WHERE
	TICKET_COUNT IN (
		SELECT DISTINCT
			COUNT(EVENT) AS COUNT
		FROM
			TICKETS
		GROUP BY
			EVENT
		ORDER BY
			COUNT DESC
		LIMIT
			3
	)
ORDER BY TICKET_COUNT DESC, NAME ASC